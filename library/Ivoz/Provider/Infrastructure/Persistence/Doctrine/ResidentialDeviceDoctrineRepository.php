    /**
     * @param int $companyId
     * @return string[]
     */
    public function findNamesByCompanyId(int $companyId)
    {
        $qb = $this->createQueryBuilder('self');
        $expression = $qb->expr();

        $qb
            ->select('self.name')
            ->where(
                $expression->eq('self.company', $companyId)
            );

